    private void carregarTabela() {
            try {
                java.util.List<Garagem> garagens = new GaragemDao().listarGaragem();

                // cria um modelo de tabela b√°sico
                String[] colunas = {"ID","Nome","Pa√≠s","Estado","Cidade","Bairro","Rua","N√∫mero","CEP"};
                javax.swing.table.DefaultTableModel modeloTabela = new javax.swing.table.DefaultTableModel(colunas, 0);

                // preenche as linhas
                for (Garagem g : garagens) {
                    Object[] linha = {
                        g.getId(),
                        g.getNome(),
                        g.getPais(),
                        g.getEstado(),
                        g.getCidade(),
                        g.getBairro(),
                        g.getRua(),
                        g.getNumero(),
                        g.getCep()
                    };
                    modeloTabela.addRow(linha);
                }

                tblGaragens.setModel(modeloTabela);


            } catch (Exception erro) {
                
                javax.swing.JOptionPane.showMessageDialog(this, "N√£o foi poss√≠vel carregar as garagens.\nDetalhes: " + erro.getMessage());
            }
    }
