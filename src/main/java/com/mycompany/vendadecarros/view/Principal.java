    private void btnAdicionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAdicionarMouseClicked
        int row = tblGaragens.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma linha para atualizar.");
            return;
        }
        try {
            int id = (int) tblGaragens.getValueAt(row, 0);
            Garagem g = new Garagem(
                id,
                txtNome.getText(),
                txtPais.getText(),
                txtEstado.getText(),
                txtCidade.getText(),
                txtBairro.getText(),
                txtRua.getText(),
                txtNumero.getText(),
                txtCep.getText()
            );
            new GaragemDao().atualizaGaragem(g);
            carregarTabela();
            limparCampos();
            javax.swing.JOptionPane.showMessageDialog(this, "Garagem atualizada com sucesso!");
        } catch (Exception ex) {
            
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao atualizar: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAdicionarMouseClicked

